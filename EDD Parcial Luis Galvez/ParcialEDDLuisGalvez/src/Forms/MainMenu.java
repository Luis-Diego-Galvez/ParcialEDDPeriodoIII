    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtCant.getText().length() > 0) {
            try {
                List.setRowCount(0);
                int Cant = Integer.parseInt(txtCant.getText());
                for (int i = 1; i <= Cant; i++) {
                    Transportaciones TP = new Transportaciones(i, CDB);
                    String[] Row = {String.valueOf(TP.getID()), TP.getInicio(), TP.getDestino(), TP.getMaterial(), String.format("%.2f", TP.getCosto()), String.valueOf(TP.getDia())};
                    if (Row != null)
                        List.addRow(Row);
                    else
                        Cant++;
                }
            }catch (Exception e) {
                List.setRowCount(0);
                JOptionPane.showMessageDialog(null, e, "", JOptionPane.WARNING_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            int dia = Integer.parseInt(txtSelectDay.getText());
            Viajes T = new Viajes(CDB);
            lblCount.setText(String.valueOf(T.CountViajes(dia)));
            lblTotal.setText(String.format("%.2f", T.TotalCosts(dia)));
            lblAverage.setText(String.format("%.2f", T.AverageCosts(dia)));
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

