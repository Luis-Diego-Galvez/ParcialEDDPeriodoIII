    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int Dia = Integer.parseInt(txtDia.getText());
            double Costo = Double.parseDouble(txtCosto.getText());
            if (txtInicio.getText().length() > 0 && txtDestino.getText().length() > 0 && txtMaterial.getText().length() > 0) {
                Transportaciones TP = new Transportaciones(txtInicio.getText(), txtDestino.getText(), txtMaterial.getText(), Costo, Dia);
                if (lblID.getText().length() > 0) {
                    try {
                        int ID = Integer.parseInt(lblID.getText());
                        TP.setID(ID);
                        TP.UpdateIn(CDB);
                    }catch (Exception ee) {
                        JOptionPane.showMessageDialog(null, ee,"ERROR",JOptionPane.WARNING_MESSAGE);
                    }
                }else {
                    TP.InsertIn(CDB);
                }
            }
            }catch(Exception e) {}
    }//GEN-LAST:event_jButton2ActionPerformed

